def run():
    author = User.objects.filter(is_superuser=True).first()
    if not author:
        print("❌ Нет суперпользователя. Создайте через python manage.py createsuperuser")
        return

    count = 0
    for mc_title, ideas in IDEAS_DATA.items():
        mc = MasterClass.objects.filter(title=mc_title).first()
        if not mc:
            print(f"⚠️ Мастер-класс '{mc_title}' не найден, но идеи всё равно создадим")
        for idea_data in ideas:
            image_filename = idea_data.get('image', '')
            if image_filename:
                if not image_filename.startswith('ideas/'):
                    image_filename = f'ideas/{image_filename}'
            defaults = {
                'content': idea_data['content'],
                'author': author,
                'is_published': True,
            }
            if image_filename:
                defaults['cover_image'] = image_filename
            obj, created = Idea.objects.update_or_create(
                title=idea_data['title'],
                defaults=defaults
            )
            # Работа с тегами
            if created:
                obj.tags.add(*idea_data['tags'])   # add принимает отдельные аргументы
                print(f"✅ Добавлена: {obj.title} (теги: {', '.join(idea_data['tags'])})")
                count += 1
            else:
                # Обновляем теги: set ожидает список, передаём без звёздочки
                obj.tags.set(idea_data['tags'])
                print(f"🔄 Обновлена: {obj.title}")
    print(f"\n🎉 Всего добавлено/обновлено {count} идей. Перейдите на /library/")

if __name__ == '__main__':
    run()